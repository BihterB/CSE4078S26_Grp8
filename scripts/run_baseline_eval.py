import argparse
import os
import time

import pandas as pd
import torch
from datasets import load_dataset
from tqdm import tqdm
from transformers import AutoModelForCausalLM, AutoTokenizer


DATASET_NAME = "Renicames/turkish-law-chatbot"


def build_prompt(question: str) -> str:
    """
    This prompt is used for Turkish legal question answering.
    We keep it simple and same for all baseline models.
    """
    return f"""Aşağıdaki Türkçe hukuk sorusunu kısa, açık ve doğru şekilde cevapla.

Soru:
{question}

Cevap:"""


def generate_answer(model, tokenizer, question: str, max_new_tokens: int = 120) -> str:
    prompt = build_prompt(question)

    inputs = tokenizer(prompt, return_tensors="pt")

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
        )

    full_output = tokenizer.decode(outputs[0], skip_special_tokens=True)

    # Try to remove the original prompt from the generated text.
    answer = full_output.replace(prompt, "").strip()

    return answer


def main():
    parser = argparse.ArgumentParser()

    parser.add_argument(
        "--model_name",
        type=str,
        required=True,
        help="Hugging Face model name, for example Qwen/Qwen2.5-1.5B-Instruct",
    )

    parser.add_argument(
        "--model_short_name",
        type=str,
        required=True,
        help="Short name used in output file, for example qwen_1_5b",
    )

    parser.add_argument(
        "--num_examples",
        type=int,
        default=10,
        help="Number of test examples to evaluate.",
    )

    parser.add_argument(
        "--max_new_tokens",
        type=int,
        default=120,
        help="Maximum number of new tokens generated by the model.",
    )

    args = parser.parse_args()

    print("Loading dataset...")
    dataset = load_dataset(DATASET_NAME)
    test_data = dataset["test"]

    print(f"Dataset loaded. Test size: {len(test_data)}")
    print(f"Using first {args.num_examples} examples for this baseline run.")

    print("\nLoading tokenizer...")
    tokenizer = AutoTokenizer.from_pretrained(args.model_name)

    print("Loading model...")
    model = AutoModelForCausalLM.from_pretrained(
        args.model_name,
        dtype=torch.float32,
        device_map=None,
    )

    model.eval()

    results = []

    print("\nRunning baseline evaluation...")
    start_time = time.time()

    for i in tqdm(range(args.num_examples)):
        question = test_data[i]["Soru"]
        reference_answer = test_data[i]["Cevap"]

        try:
            model_answer = generate_answer(
                model=model,
                tokenizer=tokenizer,
                question=question,
                max_new_tokens=args.max_new_tokens,
            )

            error_message = ""

        except Exception as e:
            model_answer = ""
            error_message = str(e)

        results.append(
            {
                "index": i,
                "model_name": args.model_name,
                "question": question,
                "reference_answer": reference_answer,
                "model_answer": model_answer,
                "error": error_message,
            }
        )

    end_time = time.time()
    elapsed_time = end_time - start_time

    os.makedirs("results", exist_ok=True)

    output_path = f"results/{args.model_short_name}_baseline_outputs_{args.num_examples}.csv"

    df = pd.DataFrame(results)
    df.to_csv(output_path, index=False, encoding="utf-8")

    print("\nBaseline evaluation finished.")
    print(f"Elapsed time: {elapsed_time:.2f} seconds")
    print(f"Saved output file: {output_path}")


if __name__ == "__main__":
    main()